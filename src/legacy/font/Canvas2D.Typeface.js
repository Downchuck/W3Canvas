(function() {

// global functions

Canvas2D.Typeface = {

	getProps: function(name) {
	
		var o = Font[name || Package.Name];

		return {

			descent: o.descent * scaleFactor * 0.1,
			ascent: o.ascent * scaleFactor * 0.1,
			unitsPerEm: o.unitsPerEm * scaleFactor * 0.1
			
		};	
	},
	cache: function(name, r) {
	
		Package = Font[name] = r;
	
	}	
};

// prototype functions

CanvasProto._measureText = function(text) {

	loadStyle(this.font);

	var glyphs = Package.glyphs;

	for(var i = 0, len = text.length, n = 0; i < len; i ++ )

		n += glyphs[ text.charCodeAt(i) ].xadv || 0; //- ?

	return { width: n * scaleFactor };

};

CanvasProto._fillText = function(text, x, y) {

	this.save();
	this.beginPath();
	this.translate(x, y);
	loadStyle(this.font);
	this.scale(scaleFactor, -scaleFactor);
	drawText(this, text);
	this.closePath();
	this.restore();
	this.fill();
	this.beginPath();

};

CanvasProto._strokeText = function(text, x, y) {

	this.save();
	this.beginPath();
	this.translate(x, y);
	loadStyle(this.font);
	this.scale(scaleFactor, -scaleFactor);
	drawText(this, text);
	this.closePath();
	this.restore();
	this.stroke();
	this.beginPath();

};

CanvasProto._drawText = function(text) {

	this.save();
	this.beginPath();
	loadStyle(this.font);
	this.scale(scaleFactor, -scaleFactor);
	drawText(this, text);
	this.closePath();
	this.restore();

};

CanvasProto._loadFont = function(name, r) { // load typeface

	if(name.indexOf('http') > -1) {  } // load from url
	
	else Package = Font[name] = r;
	
};

// used to prototype fillText(), strokeText(), and drawText()

var Font = { }; // font sets

var Package = { }; // current font

var Cache = { }; // paths cached in VOB

var drawText = function(ctx, text) { 

	var glyphs = Package.glyphs, glyph, charCode, o;

	if(typeof text == 'number') text = String(text); // convert numbers to strings

	for(var i = 0, len = text.length, r = { }; i < len; i ++ ) { 

		glyph = glyphs[ charCode = text[ i ].charCodeAt() ];

		if(charCode == 10 || charCode == 13) continue; // non-printing characters

		if(!(charCode in Cache || charCode in glyphs)) charCode = '?'.charCodeAt(); // default to question-mark

		if(!(charCode in Cache)) Cache[ charCode ] = SVGPath_VOB(glyph.path);
		
		r = Cache[ charCode ];

		for(var n in r) {
			
			switch((o = r[ n ]).id) {
			
				case 'C': ctx.bezierCurveTo(o.X1, o.Y1, o.X2, o.Y2, o.X, o.Y); break; // Cubic Bézier CurveTo()
					
				case 'Q': ctx.quadraticCurveTo(o.X1, o.Y1, o.X, o.Y); break; // Quadratic Bézier CurveTo()
			
				case 'L': ctx.lineTo(o.X, o.Y); break; // LineTo()
	
				case 'M': ctx.moveTo(o.X, o.Y); break; // MoveTo()
	
				case 'Z': ctx.closePath(); break; // ClosePath()
	
			}
		}

		ctx.translate(glyph.xadv, 0);

	}
};

// typeface style settings

var scaleFactor = 1.0, cacheFont, cacheFontSize, cacheFontFamily;

var loadStyle = function(font) {

	if(!font || font == cacheFont) return; // nothings changed

	else cacheFont = font; // cache font settings

	var r = font.split(' '); // break down css.font
		
	var fontSize = r[0], fontFamily = r[1];
	
	if(cacheFontFamily != fontFamily) { // has font family changed? load font
	
//-		loadFont(fontFamily);
	
	}

	if(cacheFontSize != fontSize) { // has font size changed? calculate scaleFactor

		cacheFontSize = fontSize; // cache fontSize
	
		var type = fontSize, n = parseFloat(type);

		type = type.substr(type.length - 2, 2).toUpperCase();

		if(type.length == 2 && !isNaN(n)) {
		
			if(type != 'PT') { // convert to points
			
				if(type != 'PX') n *= Convert[type + '_PX'];
				
				n *= Convert['PX_PT' ];
				
			}

			scaleFactor = n / 1600;

		}				
	}
};

})();

